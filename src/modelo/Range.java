package modelo;

public class Range {
	int minRange;
	int maxRange;

	public Range(int minRange, int maxRange) {
		super();
		this.minRange = minRange;
		this.maxRange = maxRange;
	}

	public int getMinRange() {
		return minRange;
	}

	public int getMaxRange() {
		return maxRange;
	}

}
